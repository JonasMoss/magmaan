#!/usr/bin/env bash
# Vendor the magmaan C++ core + vendored third_party (PORT, QUADPACK) into a
# self-contained R package src/ so `R CMD INSTALL` builds with nothing but a
# C++23 compiler and a system NLopt — no CMake, no prebuilt libmagmaan.a, and no
# reach-up to ../../{src,include}. That reach-up is what breaks the moment the
# package is installed on its own (remotes::install_github, a tarball, an HPC
# scp), because only r-package/ ships.
#
# Canonical sources stay the single source of truth:
#   <repo>/src                 — implementation (*.cpp + private *.hpp)
#   <repo>/include/magmaan     — public headers
#   <repo>/third_party/{port,quadpack} — f2c-translated C optimizer/quadrature
# Every file written here carries an @generated banner and is overwritten on the
# next run. NEVER edit the vendored copies — edit the canonical sources and
# re-run `just vendor`. NLopt is a SYSTEM dependency (SystemRequirements: NLopt);
# the Makevars finds it via pkg-config. Ceres/IPOPT optimizer adapters are
# dropped (they need external libs we don't ship); PORT + NLopt remain.
#
# Layout produced under <dest> (default r-package/src):
#   core/<mirror of src/>      — vendored impl + private headers, subdirs kept
#   magmaan/<mirror of include/magmaan/> — public headers, so -I. resolves
#                                          #include "magmaan/..."
#   third_party/port/...       — PORT C sources (+ f2c.h)
#   third_party/quadpack/...   — QUADPACK C sources, d1mach_/pow_dd renamed to
#                                magmaan_qpk_* so the two archives never collide
# Hand-written glue (bindings.cpp, fit.cpp, robust.cpp, sim.cpp,
# lr_test_satorra.cpp, RcppExports.cpp, internal.hpp, Makevars) has no banner and
# is left untouched.
set -euo pipefail

here="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"   # repo root
dest="${1:-$here/r-package/src}"                          # staging-dir override OK
marker="// @generated by dev/vendor-cpp.sh"

# Optimizer adapters that need external libraries the self-contained package does
# not ship. PORT (vendored) and NLopt (system) stay; these are guarded by
# #ifdef MAGMAAN_WITH_{CERES,IPOPT}, which we never define, so they would compile
# to empty TUs anyway — dropping them keeps the package lean.
exclude_rel=( "optim/ceres_optimizer.cpp" "optim/ipopt_optimizer.cpp" )

# 1. Drop anything a previous run generated (banner-detected) so renamed/deleted
#    canonical files leave no stale copy behind, then clear the generated trees.
if [ -d "$dest" ]; then
  while IFS= read -r -d '' f; do
    if IFS= read -r first < "$f" && [[ "$first" == *"@generated by dev/vendor-cpp.sh"* ]]; then
      rm -f "$f"
    fi
  done < <(find "$dest" -type f \( -name '*.cpp' -o -name '*.hpp' -o -name '*.c' -o -name '*.h' \) -print0)
fi
rm -rf "$dest/core" "$dest/magmaan" "$dest/third_party"

stamp() {  # stamp <src> <dest> [sed-program]
  local s="$1" d="$2" prog="${3:-}" rel
  rel="$(realpath --relative-to="$here" "$s")"
  mkdir -p "$(dirname "$d")"
  printf '%s from %s — DO NOT EDIT; re-run `just vendor`.\n' "$marker" "$rel" > "$d"
  if [ -n "$prog" ]; then sed -E "$prog" "$s" >> "$d"; else cat "$s" >> "$d"; fi
}

count=0
# 2a. Core implementation: src/**/*.cpp -> core/ (preserve subdirs), minus the
#     excluded optimizer backends.
while IFS= read -r -d '' s; do
  rel="${s#"$here"/src/}"
  skip=0; for e in "${exclude_rel[@]}"; do [ "$rel" = "$e" ] && skip=1; done
  [ "$skip" = 1 ] && continue
  stamp "$s" "$dest/core/$rel"; count=$((count + 1))
done < <(find "$here/src" -name '*.cpp' -print0)

# 2b. Core private headers: src/**/*.hpp -> core/ (same relative path).
while IFS= read -r -d '' s; do
  rel="${s#"$here"/src/}"; stamp "$s" "$dest/core/$rel"; count=$((count + 1))
done < <(find "$here/src" -name '*.hpp' -print0)

# 2c. Public headers: include/magmaan/** -> magmaan/** (so -I. resolves them).
while IFS= read -r -d '' s; do
  rel="${s#"$here"/include/magmaan/}"; stamp "$s" "$dest/magmaan/$rel"; count=$((count + 1))
done < <(find "$here/include/magmaan" -name '*.hpp' -print0)

# 2d. PORT (pure f2c C): every cport/ TU + header, the three top-level TUs.
for s in "$here"/third_party/port/cport/*.c "$here"/third_party/port/cport/*.h \
         "$here"/third_party/port/drmnfb_routines.c \
         "$here"/third_party/port/port_io.c \
         "$here"/third_party/port/f2c_intrinsics.c; do
  [ -e "$s" ] || continue
  rel="${s#"$here"/third_party/port/}"
  stamp "$s" "$dest/third_party/port/$rel"; count=$((count + 1))
done

# 2e. QUADPACK (pure f2c C): rename d1mach_/pow_dd (shared with PORT) to private
#     symbols so the two vendored archives never collide, mirroring the
#     -D defines QuadpackVendor.cmake applies. f2c.h is identical to PORT's.
for s in "$here"/third_party/quadpack/*.c "$here"/third_party/quadpack/*.h; do
  [ -e "$s" ] || continue
  rel="${s#"$here"/third_party/quadpack/}"
  stamp "$s" "$dest/third_party/quadpack/$rel" \
    's/\<d1mach_\>/magmaan_qpk_d1mach_/g; s/\<pow_dd\>/magmaan_qpk_pow_dd/g'
  count=$((count + 1))
done

echo "vendored $count file(s) into ${dest#"$here"/}"
