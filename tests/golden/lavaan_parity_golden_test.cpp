// abs-or-rel agreement: passes when |a-b| ≤ tol·max(1,|b|).
bool close(double a, double b, double tol) {
  return std::abs(a - b) <= tol * std::max(1.0, std::abs(b));
}

Eigen::MatrixXd matrix_from_json(const nlohmann::json& values) {
  const auto p = static_cast<Eigen::Index>(values.size());
  const auto c0 =
      p > 0 ? static_cast<Eigen::Index>(values[0].size()) : Eigen::Index{0};
  Eigen::MatrixXd M(p, c0);
  for (Eigen::Index r = 0; r < p; ++r)
    for (Eigen::Index c = 0; c < c0; ++c)
      M(r, c) = values[static_cast<std::size_t>(r)]
                       [static_cast<std::size_t>(c)].get<double>();
  return M;
}

Eigen::VectorXd vector_from_json(const nlohmann::json& arr) {
  Eigen::VectorXd v(static_cast<Eigen::Index>(arr.size()));
  for (Eigen::Index k = 0; k < v.size(); ++k)
    v(k) = arr[static_cast<std::size_t>(k)].get<double>();
  return v;
}

// [{block, matrix}, ...] → vector of dense matrices (lavaan WLS.V / gamma).
std::vector<Eigen::MatrixXd> matrices_from_blocks(const nlohmann::json& blocks) {
  std::vector<Eigen::MatrixXd> out;
  out.reserve(blocks.size());
  for (const auto& b : blocks) out.push_back(matrix_from_json(b["matrix"]));
  return out;
}

double max_abs_diff(const Eigen::VectorXd& a, const Eigen::VectorXd& b) {
  if (a.size() != b.size()) return std::numeric_limits<double>::infinity();
  double out = 0.0;
  for (Eigen::Index k = 0; k < a.size(); ++k)
    out = std::max(out, std::abs(a(k) - b(k)));
  return out;
}

// Reads a parity fixture's reference.json + (optional) data.json. Returns
// false with a queued failure when reference.json is missing/invalid.
bool load_fixture(const std::string& dir, const std::string& id,
                  nlohmann::json& ref, nlohmann::json& data,
                  std::vector<std::string>& failures) {
  auto ref_raw = magmaan::test::read_fixture(dir + "/" + id + "/reference.json");
  if (!ref_raw.has_value()) {
    failures.push_back(id + ": missing reference.json — run "
                            "tools/regen_parity_fixtures.R");
    return false;
  }
  ref = nlohmann::json::parse(*ref_raw, nullptr, /*allow_exceptions=*/false);
  if (ref.is_discarded()) {
    failures.push_back(id + ": reference.json is not valid JSON");
    return false;
  }
  data = nlohmann::json{};
  auto data_raw = magmaan::test::read_fixture(dir + "/" + id + "/data.json");
  if (data_raw.has_value()) {
    data = nlohmann::json::parse(*data_raw, nullptr, false);
    if (data.is_discarded()) {
      failures.push_back(id + ": data.json is not valid JSON");
      return false;
    }
  }
  return true;
}

// Threshold-augmented ordinal syntax: appends `xj | t1 + …` and `xj ~*~ 1*xj`
// rows for each variable, level count read from the integer data blocks.
// Mirrors ordinal_golden_test.cpp's ordinal_syntax for the parity fixture
// layout (bare model in reference.json, integer blocks in data.json).
std::string ordinal_syntax(const std::string& bare_model,
                           const nlohmann::json& blocks) {
  std::string src = bare_model;
  if (!src.empty() && src.back() != '\n') src.push_back('\n');
  const auto& first = blocks[0]["matrix"];
  const Eigen::Index p = static_cast<Eigen::Index>(first[0].size());
  std::vector<int> max_level(static_cast<std::size_t>(p), 0);
  for (const auto& block : blocks) {
    for (const auto& row : block["matrix"]) {
      for (Eigen::Index j = 0; j < p; ++j) {
        max_level[static_cast<std::size_t>(j)] =
            std::max(max_level[static_cast<std::size_t>(j)],
                     row[static_cast<std::size_t>(j)].get<int>());
      }
    }
  }
  for (Eigen::Index j = 0; j < p; ++j) {
    src += "x" + std::to_string(j + 1) + " | ";
    for (int lev = 1; lev < max_level[static_cast<std::size_t>(j)]; ++lev) {
      if (lev > 1) src += " + ";
      src += "t" + std::to_string(lev);
    }
    src += "\n";
  }
  for (Eigen::Index j = 0; j < p; ++j) {
    src += "x" + std::to_string(j + 1) + " ~*~ 1*x" +
           std::to_string(j + 1) + "\n";
  }
  return src;
}

